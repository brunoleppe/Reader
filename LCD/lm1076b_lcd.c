int LCD_init (uint32_t spiChannel, uint32_t dma, GPIO_PinMap cs, GPIO_PinMap bla, GPIO_PinMap dc, GPIO_PinMap rst)
{
    lcd.spiChannel = spiChannel;
    lcd.dmaChannel = dma;
    lcd.bla_pin = bla;
    lcd.cs_pin = cs;
    lcd.dc_pin = dc;
    lcd.rst_pin = rst;
    lcd.handle = SpiDriver_open(lcd.spiChannel);
    SpiDriverSetup setup = {
            .csPin = lcd.cs_pin,
            .baudRate = 20000000,
            .spiMode = SPI_MODE_3,
    };

    int r = SpiDriver_setup(lcd.handle, &setup);
    (void)r;
    return 0;
}

void    LCD_configure   ()
{
    static uint8_t config_buffer[36]={
            0x2a,0x27,0xc2,0xa1,0xd0,0xd5,0xc8,0x10,0xeb,0xa6,0xa4,0x81,0x35,0xd8,0xf1,
            0x7f,0xf2,0x00,0xf3,0x7f,0x85,0xaf,0xf4,0,0xf5,0,0xf6,79,0xf7,127,0xf8,0x00,
            0x10,0x60,0x70,0x00
    };

    GPIO_pin_write(lcd.rst_pin, GPIO_LOW);
    vTaskDelay(10);
    GPIO_pin_write(lcd.rst_pin, GPIO_HIGH);
    vTaskDelay(100);
    GPIO_pin_write(lcd.bla_pin, GPIO_LOW);
    vTaskDelay(10);

    GPIO_pin_write(lcd.dc_pin, GPIO_LOW);
    SpiDriver_transfer(lcd.handle, config_buffer, NULL, sizeof(config_buffer));
    vTaskDelay(10);
}

void    LCD_draw_point  (int x, int y, LCD_COLOR color)
{
    uint8_t parity = (x&1);
    uint8_t mask = 0x0f << (4*parity);
    uint8_t *p = lcd.lcd_buffer + (x>>1) + 120*y;
    *p &= mask;
    *p |= color << (4*(parity^1));

//    if(color == LCD_COLOR_BLACK)
//        *p |= 0xF << (((x&1)^1) * 4);
//    else
//        *p &= 0xff ^ (0xE << (((x&1)^1) * 4));
}
void    LCD_draw_hline  (int x, int y, int length, LCD_COLOR color)
{
    int i;
    for(i=0;i<length;i++){
        LCD_draw_point(x+i,y,color);
    }
}
void    LCD_draw_vline  (int x, int y, int length, LCD_COLOR color)
{
    int i;
    for(i=0;i<length;i++){
        LCD_draw_point(x,y+i,color);
    }
}
void    LCD_draw_rect   (int x, int y, int height, int width, LCD_COLOR color)
{
    LCD_draw_hline(x, y, width, color);
    LCD_draw_hline(x, y+height-1, width, color);
    LCD_draw_vline(x, y, height, color);
    LCD_draw_vline(x+width-1, y, height, color);
}
void    LCD_draw_fill   (int x, int y, int height, int width, LCD_COLOR color)
{
    while(height--){
        LCD_draw_hline(x, y++, width, color);
    }
}
void    LCD_draw_char   (int x, int y, char c, LCD_Fonts f, LCD_COLOR color)
{
    const LCD_Font *font = fonts[f];
    uint8_t cols,rows;
    uint8_t i,j,*ch;
    uint32_t val;
    cols = font->cols;
    rows = font->rows;
    ch = LCD_get_char(c, font);

    for(i=0;i<cols;i++){
        memcpy(&val,ch,font->bpr);
        ch+=font->bpr;
        for(j=0;j<rows;j++){
            if(val&(1<<j))
                LCD_draw_point(x+i,y+j,color);
        }
    }
}
void    LCD_draw_string (int x, int y, const char *str, LCD_Fonts f, LCD_COLOR color)
{
    const LCD_Font *font = fonts[f];
    uint8_t i,len = strlen(str);
//    LCD_COLOR negative = LCD_color_inverse(color);
    uint8_t space = 1;
//    LCD_draw_fill(x, y, font->rows, len*(space+font->cols), negative);
    for(i=0;i<len;i++){
        LCD_draw_char(x, y,str[i], f, color);
        x+=space+font->cols;
    }
}
