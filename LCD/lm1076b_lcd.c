void    LCD_print       ( void )
{

    memcpy(lcd.render_buffer, lcd.lcd_buffer, LCD_BUFFER_SIZE);

    GPIO_pin_write(lcd.dc_pin, GPIO_HIGH);
    if(SpiDriver_mutex_take(lcd.handle, portMAX_DELAY)) {
        SpiDriver_transfer(lcd.handle, lcd.render_buffer, NULL, LCD_BUFFER_SIZE);
        SpiDriver_mutex_release(lcd.handle);
    }
}

static unsigned char* LCD_get_char(unsigned char c, const LCD_Font *font){
    if(c <= '~')
        return font->font + (c-' ')*font->bytes;
    else{
        if(font->special == NULL)
            return font->font;
        uint8_t index;
        switch (c){
            //case 191: index = 0;break;
            case 193: index = 7;break;
            case 201: index = 8;break;
            case 205: index = 9;break;
            case 209: index = 10;break;
            case 211: index = 11;break;
            case 218: index = 12;break;
            case 220: index = 13;break;
            case 225: index = 0;break;
            case 233: index = 1;break;
            case 237: index = 2;break;
            case 241: index = 3;break;
            case 243: index = 4;break;
            case 250: index = 5;break;
            case 252: index = 6;break;
            case 254: index = 15;break;
            default: index = 14;
        }
        return font->special+index*font->bytes;
    }
}

